// Defines supported Lua property value types for TombEngine property system.
// These correspond to TEN Lua API types: bool, int, float, string, Vec2, Vec3, Rotation, Color, Time, Enum.

namespace TombLib.LuaProperties
{
    public enum LuaPropertyType
    {
        Bool,
        Int,
        Float,
        String,
        Vec2,
        Vec3,
        Rotation,
        Color,
        Time,
        Enum
    }
}
